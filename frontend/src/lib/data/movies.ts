export function getMovieById(id: string): Movie | undefined {
  return movies.find((m) => m.id === id);
}

export function getFeaturedMovies(): Movie[] {
  return movies.filter((m) => m.isFeatured);
}

export function getTrendingMovies(): Movie[] {
  return movies.filter((m) => m.isTrending);
}

export function getNewMovies(): Movie[] {
  return movies.filter((m) => m.isNew);
}

export function getMoviesByGenre(genre: string): Movie[] {
  return movies.filter((m) => {
    const genresList = Array.isArray(m.genres) ? m.genres : [m.genres];
    return genresList.some((g: any) => g?.toLowerCase() === genre.toLowerCase());
  });
}

export function getSimilarMovies(movie: Movie): Movie[] {
  return movies
    .filter(
      (m) => {
        const mGenres = Array.isArray(m.genres) ? m.genres : [m.genres];
        const movieGenres = Array.isArray(movie.genres) ? movie.genres : [movie.genres];
        return m.id !== movie.id && mGenres.some((g: any) => movieGenres.includes(g));
      }
    )
    .slice(0, 8);
}
